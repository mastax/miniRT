void	report_error(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		write(2, &str[i], 1);
	exit (1);
}

void	next(char **str)
{
	while (**str == 32 || **str == 9)
		(*str)++;
}

void	in_range(double nb, double min, double max, char *function)
{
	char	message[100];

	if (nb < min || nb > max)
	{
		ft_strncpy(message, function, 76);
		ft_strcat(message, " parameter out of range\n");
		report_error(message);
	}
}

